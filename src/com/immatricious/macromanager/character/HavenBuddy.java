package com.immatricious.macromanager.character;

public class HavenBuddy {
	private boolean logged;
	private final String name;
	
	public HavenBuddy(String name)
	{
		this.name = name;
	}
	
	public String getName() { return this.name; }
	public boolean islogged() { return this.islogged(); }
	
	public void setLogged(boolean b) { this.logged = b; }
}
